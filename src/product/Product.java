package product;

public class Product {
	private String productName;
	private String productPrice;
	private String productImage;
	private String productImageRealName;
	private String sellerID;
	private String sellerName;
	private String tradingPlace;
	private String productStatus;
	private String productBuyer;
	
	
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductPrice() {
		return productPrice;
	}
	public void setProductPrice(String productPrice) {
		this.productPrice = productPrice;
	}
	public String getProductImage() {
		return productImage;
	}
	public void setProductImage(String productImage) {
		this.productImage = productImage;
	}
	public String getProductImageRealName() {
		return productImageRealName;
	}
	public void setProductImageRealName(String productImageRealName) {
		this.productImageRealName = productImageRealName;
	}
	public String getSellerID() {
		return sellerID;
	}
	public void setSellerID(String sellerID) {
		this.sellerID = sellerID;
	}
	public String getSellerName() {
		return sellerName;
	}
	public void setSellerName(String sellerName) {
		this.sellerName = sellerName;
	}
	public String getTradingPlace() {
		return tradingPlace;
	}
	public void setTradingPlace(String tradingPlace) {
		this.tradingPlace = tradingPlace;
	}
	public String getProductStatus() {
		return productStatus;
	}
	public void setProductStatus(String productStatus) {
		this.productStatus = productStatus;
	}
	public String getProductBuyer() {
		return productBuyer;
	}
	public void setProductBuyer(String productBuyer) {
		this.productBuyer = productBuyer;
	}
	
	
}
